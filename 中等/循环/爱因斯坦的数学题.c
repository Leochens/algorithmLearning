/*
爱因斯坦的数学题。爱因斯坦出了一道这样的数学题。有一条长阶梯，若每步跨2阶，则最后剩1阶，若每步跨3阶，则最后剩2阶，若每步跨5阶，则最后剩4阶，若每步跨6阶，则最后剩5阶，若每步跨7阶，最后才正好一阶不剩。请问，这条阶梯共多少阶？
**输出格式要求："x = %d\n"
 */
#include <stdio.h>
#include <math.h>
  
int main()
{
    int x;
    //2*x+1==3*x+2==5*x+4==6*x+5==7*x = p
    for(x=1;x<10000;x++)
    {
        if(x%2==1&&x%3==2&&x%5==4&&x%6==5&&x%7==0)
        {
            printf("x = %d\n",x);  
            return 0;
        }
    }
     
     
     
        
     
    return 0;
}

